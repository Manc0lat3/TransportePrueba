        String marca = txtMarca.getText();
        int capacidad = Integer.parseInt(txtCapacidad.getText());
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd/MM/yyyy");
        LocalDate fechaRegistro = LocalDate.parse(txtFechaReg.getText(), formatter);
        Camion nuevoCamion = new Camion(patente, modelo, marca, capacidad, fechaRegistro);
        guardarCamion(nuevoCamion);

    this.dispose();
