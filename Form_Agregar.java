    private void guardarCamion(Camion camion) {
        Connection conn = null;
        PreparedStatement pstmt = null;

        try {
            Class.forName("com.mysql.cj.jdbc.Driver");

            conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/transportedb", "root", "");

            // Verificar si ya existe un camión con la misma patente y fecha
            if (!existeCamion(conn, camion.getPatente(), camion.getFechaReg())) {
                String sql = "INSERT INTO camion (patente, modelo, marca, capacidad, fecha_reg) VALUES (?, ?, ?, ?, ?)";
                pstmt = conn.prepareStatement(sql);
                pstmt.setString(1, camion.getPatente());
                pstmt.setString(2, camion.getModelo());
                pstmt.setString(3, camion.getMarca());
                pstmt.setInt(4, camion.getCapacidad());
                pstmt.setDate(5, java.sql.Date.valueOf(camion.getFechaReg()));

                pstmt.executeUpdate();
            } else {
                JOptionPane.showMessageDialog(null, "Ya existe un camión con la misma patente y fecha de registro", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (ClassNotFoundException | SQLException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "Error de base de datos", "Error", JOptionPane.ERROR_MESSAGE);
        } finally {
            try {
                if (pstmt != null) {
                    pstmt.close();
                }
                if (conn != null) {
                    conn.close();
                }
            } catch (SQLException ex) {
                ex.printStackTrace();
            }
        }
    }

    private boolean existeCamion(Connection conn, String patente, LocalDate fechaReg) throws SQLException {
        String sql = "SELECT COUNT(*) FROM camion WHERE patente = ? AND fecha_reg = ?";
        try (PreparedStatement pstmt = conn.prepareStatement(sql)) {
            pstmt.setString(1, patente);
            pstmt.setDate(2, java.sql.Date.valueOf(fechaReg));

            try (var rs = pstmt.executeQuery()) {
                rs.next();
                return rs.getInt(1) > 0;
            }
        }
    }

