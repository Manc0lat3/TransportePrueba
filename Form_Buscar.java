    private void buscarCamion() {
        String patente = txtBuscarPatente.getText().trim();

        Connection conn = null;
        PreparedStatement pstmt = null;
        ResultSet rs = null;

        try {
            Class.forName("com.mysql.cj.jdbc.Driver");

            conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/transportedb", "root", "");

            String sql = "SELECT * FROM camion WHERE patente = ?";
            pstmt = conn.prepareStatement(sql);
            pstmt.setString(1, patente);

            rs = pstmt.executeQuery();

            if (rs.next()) {
                String Modelo = rs.getString("modelo");
                String Marca = rs.getString("marca");
                int Capacidad = rs.getInt("capacidad");

                JOptionPane.showMessageDialog(
                        null,
                        "Camion encontrado:\n"
                        + "Patente: " + rs.getString("Patente") + "\n"
                        + "Marca: " + rs.getString("Marca") + "\n"
                        + "Modelo: " + rs.getString("Modelo") + "\n"
                        + "Capacidad: " + rs.getString("Capacidad") + "\n",
                        "Informaci√≥n",
                        JOptionPane.INFORMATION_MESSAGE
                );
            } else {
                JOptionPane.showMessageDialog(null, "Caminon no encontrado", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (ClassNotFoundException | SQLException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "Error de base de datos", "Error", JOptionPane.ERROR_MESSAGE);
        } finally {
            try {
                if (rs != null) {
                    rs.close();
                }
                if (pstmt != null) {
                    pstmt.close();
                }
                if (conn != null) {
                    conn.close();
                }
            } catch (SQLException ex) {
                ex.printStackTrace();
            }
        }
    }

